#include <iostream>
using namespace std;

int main() {

  	// for loop to print "Hi" 5 times
    for (int i = 1; i <= 10; i++) { // เริ่มลูปโดยกำหนดให้ i=1 และทำซ้ำจนกว่า i จะมากกว่า 10 และ i จะเพิ่มทีละ 1 ในแต่ละรอบ
      	cout << "Hi => i = " << i << endl; // แสดงข้อความพร้อมค่าของ i
    }
  
    return 0; // จบการทำงาน
}